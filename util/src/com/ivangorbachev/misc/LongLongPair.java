package com.ivangorbachev.misc;

/**
 * @author Ivan Gorbachev
 */
public class LongLongPair {

    private long x, y;

    public LongLongPair() {
    }

    public LongLongPair(long x, long y) {
        this.x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }
}
