package com.ivangorbachev.misc;

/**
 * @author Ivan Gorbachev
 */
public class IntValue {

    private int value;

    public IntValue() {
    }

    public IntValue(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
}
