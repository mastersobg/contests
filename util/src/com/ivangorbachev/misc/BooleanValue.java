package com.ivangorbachev.misc;

/**
 * @author Ivan Gorbachev
 */
public class BooleanValue {

    private boolean value;

    public BooleanValue() {
    }

    public BooleanValue(boolean value) {
        this.value = value;
    }

    public boolean isValue() {
        return value;
    }

    public void setValue(boolean value) {
        this.value = value;
    }
}
