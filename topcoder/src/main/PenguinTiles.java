    public int minMoves(String[] tiles) {
      int x = 0, y = 0;
      for(int i = 0; i < tiles.length; ++i) {
        for(int j = 0; j < tiles[i].length(); ++j) 
          if(tiles[i].charAt(j) == '.') {
            x = i;
            y = j;
            break;
          }
      }
      int n = tiles.length - 1;
      int m = tiles[0].length() - 1;
      if(x == n && y == m)
        return 0;
      else if(x == n || y == m)
        return 1;
      else return 2;
    }

