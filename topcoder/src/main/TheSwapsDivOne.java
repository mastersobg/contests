	int len(String[] s) {
		int ret = 0;
		for (String a : s)
			ret += a.length();
		return ret;
	}

	int[] set(String[] s) {
		int[] ret = new int[len(s)];
		int idx = 0;
		for (String a : s) {
			for (int i = 0; i < a.length(); ++i)
				ret[idx++] = a.charAt(i);
		}
		return ret;
	}

	public double find(String[] sequence, int k) {
		int[] v = set(sequence);
		int[] sum = new int[v.length + 1];
		for (int i = 1; i < sum.length; ++i)
			sum[i] = sum[i - 1] + v[i - 1];
		
		return 0.0;
	}

