    double rec(int idx, int []R, int []r, boolean []used) {
        if(idx == R.length) {
            return calc(r);
        }
        double ret = 1e+20;
        for(int i = 0; i < R.length; ++i)
            if(!used[i]) {
                used[i] = true;
                r[idx] = R[i];
                ret = min(ret, rec(idx + 1, R, r, used));
                used[i] = false;
            }
        return ret;
    }

    double calc(int []rad) {
        dbg("rad=", Arrays.toString(rad));
        double []pos = new double[rad.length];
        pos[0] = 0;
        for(int it = 1; it < rad.length; ++it) {
            pos[it] = can(it, 0.0, pos, rad);
        }
        double ret = pos[rad.length - 1] - pos[0];
        dbg("pos=", Arrays.toString(pos));
        dbg("ret=", ret);
        return ret;
    }

    double can(int idx, double cur, double []pos, int []r) {
        double mx = 0.0;
        for(int i = 0; i < idx; ++i) {
            double dx = pos[i] + 2.0 * sqrt((double)r[idx] * (double)r[i]);
            mx = max(mx, dx);
        }
        return mx;
    }
    
