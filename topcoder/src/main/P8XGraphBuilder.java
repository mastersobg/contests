  static class State {
    int left, start;
    int []v;

    State(int left, int start, int []v) {
      this.left = left;
      this.start = start;
      this.v = v.clone();
    }

    @Override
    public boolean equals(Object o) {
      State s = (State) o;
      return left == s.left && start == s.start && Arrays.equals(v, s.v);
    }

    @Override
    public int hashCode() {
      return left * 31 * 31 + start * 31 + Arrays.hashCode(v);
    }

    public String toString() {
      return "[left=" + left + ", " + "v=" + Arrays.toString(v) + "]";
    }
  }


  int rec(State s) {
    Integer ret = dp.get(s);
    if (ret != null) {
      return ret;
    }
    if (s.start == s.v.length) {
      return -1 << 30;
    }
    if (s.left == 0) {
      dp.put(s, 0);
      return 0;
    }
    int rret = 0;
    if (s.v[s.start] != 0) {
      rret = max(rret, rec(new State(s.left - 1, s.start, newArray(s.v, s.start))) + v[0]);
    }
    rret = max(rret, rec(new State(s.left, s.start + 1, s.v.clone())));
    dp.put(s, rret);
    return rret;
  }
  
  int []newArray(int []arr, int idx) {
    int []ret = arr.clone();
    ret[idx]--;
    ret[idx + 1]++;
    ret[0]++;
    return ret;
  }

  HashMap<State, Integer> dp;
  
  int []v;
  int n;

  public int solve(int[] scores) {
    n = scores.length + 1;
    v = scores;
    dp = new HashMap<State, Integer> ();
    int []arr = new int[n - 1];
    arr[0] = 2;
    return rec(new State(n - 2, 0, arr)) + v[0] * 2;
  }

  
