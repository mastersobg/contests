	boolean[][] g;

	public int minimalDays(String[] friendship) {
		int n = friendship.length;
		g = new boolean[n][n];
		for (int i = 0; i < n; ++i)
			for (int j = 0; j < n; ++j)
				g[i][j] = friendship[i].charAt(j) == 'Y' ? true : false;
		int[] q = new int[n + n];
		int b = 0, e = 0;
		boolean[] was = new boolean[n];
		int[] d = new int[n];
		was[0] = true;
		for (q[e++] = 0; b < e; ++b) {
			int v = q[b];
			for (int i = 0; i < n; ++i)
				if (g[v][i] && !was[i]) {
					was[i] = true;
					q[e++] = i;
					d[i] = d[v] + 1;
				}
		}
		if (was[1] == false)
			return -1;
		return get(d[1]);
	}

	int get(int cnt) {
		++cnt;
		int ret = 0;
		while (cnt > 2) {
			int s = 0;
			for (int i = 0; i + 2 < cnt; i += 3) {
				++s;
			}
			cnt -= s;
			++ret;
		}
		return ret;
	}

