    public String lexSmallestName(String S) {
        s = S.toCharArray();
        letter = new int[26];
        for (int i = 0; i < s.length; ++i) {
            int ch = s[i] - 'a';
            letter[ch]++;
        }
        left = new int[26];
        right = new int[26];
        int p = 0;
        StringBuilder ret = new StringBuilder();
        int i;
        for (i = 0; i < s.length; ++i) {
            while (p < 26 && (left[p] + right[p] >= letter[p] ||
                    left[p] >= (letter[p] >> 1))) {
                ++p;
            }
            if (p == 26)
                break;
            int ch = s[i] - 'a';
            if (s[i] - 'a' == p) {
                ret.append(s[i]);
                left[ch]++;
            } else {
                if (right[ch] < (letter[p] >> 1)) {
                    right[ch]++;
                } else {
                    ret.append(s[i]);
                    left[ch]++;
                }
            }
        }
        return ret.toString();
    }

