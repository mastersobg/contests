	int go(int l1, int l2, int pos) {
		boolean[] was = new boolean[l1];
		was[pos] = true;
		int ret = min(pos + 1, l1 - pos - 1);
		while (true) {
			pos = (pos + l2) % l1;
			ret = min(ret, pos + 1);
			ret = min(ret, l1 - pos - 1);
			if (was[pos])
				break;
			was[pos] = true;
		}
		return ret;
	}

	public int maxDist(int apple, int kiwi, int grape) {
		int[] d1 = new int[apple];
		int[] d2 = new int[apple];
		int[] d3 = new int[kiwi];
		for (int i = 0; i < apple; ++i)
			d1[i] = go(apple, kiwi, i);
		for (int i = 0; i < apple; ++i)
			d2[i] = go(apple, grape, i);
		for (int i = 0; i < kiwi; ++i)
			d3[i] = go(kiwi, grape, i);
		int ret = 0;
		for (int i = 0; i < apple; ++i) {
			for (int j = 0; j < apple; ++j)
				if (i != j) {
					int a = min(d1[i], d2[j]);
					if (j > i) {
						int q = i;
						while (q + kiwi < j)
							q += kiwi;
						a = min(a, d3[j - q - 1]);
					} else {
						int q = i;
						while (q - kiwi >= j)
							q -= kiwi;
						a = min(a, d3[j - (q - kiwi) - 1]);
					}
					ret = max(ret, a);
				}
		}
		return ret;
	}

