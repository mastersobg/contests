    public double minimumChange(int A, int B, int C) {
      double a = A;
      double b = B;
      double c = C;
      double q = abs((b - (c - b)) - a);
      double w = abs((c - (c - a) / 2.0) - b);
      double e = abs((b + (b - a)) - c);
      return min(min(q, w), e);
    }
    
