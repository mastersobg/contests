    int [][]dp;

    int rec(int l, int r) {
      if(l >= r)
        return 0;
      if(dp[l][r] == -1) {
        int ret = 0;
        if(colors[l] == colors[r]) {
          ret = max(ret, rec(l + 1, r - 1) + 1);
        }
        for(int i = l; i < r; ++i)
          ret = max(ret, rec(l, i) + rec(i + 1, r));

        dp[l][r] = ret;
      }
      return dp[l][r];
    }
    public int findMaximumMatching(String colors) {
      this.colors = colors.toCharArray();
      int n = this.colors.length;
      dp = new int[n][n];
      for(int i = 0; i < n; ++i)
        Arrays.fill(dp[i], -1);
      return rec(0, n - 1);
    }

