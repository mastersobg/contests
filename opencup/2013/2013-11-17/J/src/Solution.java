    long force(int n) {
        long cnt = -n;
        for (int i = 1; i <= n; ++i)
            cnt += (long) Integer.bitCount(i);
        return cnt;
    }

    long calc(int n, int start) {
        long cnt = 0;
        for (int i = start; i <= n; ++i)
            cnt += (long) Integer.bitCount(i);
        return cnt;
    }

